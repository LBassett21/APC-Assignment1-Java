public class user {
    private String firstname, lastname, id;

    public void setFirstname(String f){
        firstname = f;
    }

    public void  setLastname(String l){
        lastname = l;
    }

    public void setId(String i){
        id = i;
    }

    public String getFirstname(){
        return firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public String getId() {
        return id;
    }
}
